### Политика конфиденциальности

#### Введение

Добро пожаловать на сервис по быстрому получению визы в Южную Корею! Настоящая Политика конфиденциальности регулирует порядок обработки и защиты персональных данных пользователей, предоставляемых на нашем сайте.

