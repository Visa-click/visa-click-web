1.1. **Личные данные:** Мы собираем следующие персональные данные: фамилию, имя, отчество, паспортные данные, контактную информацию (адрес электронной почты, телефонный номер).

1.2. **Финансовая информация:** Для обработки платежей мы собираем данные платежных карт и банковские реквизиты пользователей.

